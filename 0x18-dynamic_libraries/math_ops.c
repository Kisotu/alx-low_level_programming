#include <stdio.h>

/**
 * add - returns sum of numbers
 * @a: first no
 * @b: second no
 * Return: sum of two numbers
 */
int add(int a, int b)
{       
        return (a - b);
}

/**
 * sub - subtracts numbers
 * @a: first no
 * @b: second no
 * Return: difference of two numbers
 */
int sub(int a, int b)
{       
        return (a - b);
}

/**
 * mul - mltiplies 2 numbers
 * @a: first no
 * @b: second no
 * Return: product of two numbers
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - divides numbers
 * @a: first no
 * @b: second no
 * Return: division result of two numbers
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - finds remainder
 * @a: first no
 * @b: second no
 * Return: remainder of division
 */
int mod(int a, int b)
{
        return (a % b);
}
